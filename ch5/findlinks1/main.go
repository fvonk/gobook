	doc, err := html.Parse(os.Stdin)
	if err != nil {
		fmt.Fprintf(os.Stderr, "findlinks1: %v\n", err)
		os.Exit(1)
	}
	
	// visitRec(doc)
	res := ElementsByTagName(doc, "img")
	fmt.Printf("res = %s\n", res)
